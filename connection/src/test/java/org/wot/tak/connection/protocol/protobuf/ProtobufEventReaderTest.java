    @Test
    void readFromProtobufProtocol_sequenceOfMessages_returnsDelimitedMessagesOneByOne() throws IOException {
        var expectedMessage1 = Takmessage.TakMessage.newBuilder()
                .setCotEvent(Cotevent.CotEvent.newBuilder()
                        .setHow("m-g")
                ).build();

        var expectedMessage2 = Takmessage.TakMessage.newBuilder()
                .setTakControl(Takcontrol.TakControl.newBuilder()
                        .setMaxProtoVersion(1)
                ).build();

        var outputStream = new PipedOutputStream();
        var inputStream = new PipedInputStream(outputStream);

        outputStream.write(ProtobufEventReader.MAGIC_BYTE);
        expectedMessage1.writeDelimitedTo(outputStream);
        outputStream.write(ProtobufEventReader.MAGIC_BYTE);
        expectedMessage2.writeDelimitedTo(outputStream);

        var actualMessage1 = ProtobufEventReader.readFromProtobufProtocol(inputStream);
        var actualMessage2 = ProtobufEventReader.readFromProtobufProtocol(inputStream);

        assertThat(actualMessage1).isEqualTo(expectedMessage1);
        assertThat(actualMessage2).isEqualTo(expectedMessage2);
    }
